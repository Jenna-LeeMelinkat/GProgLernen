package Aufgaben.ZweiKlassen.ProbeInt;



public class Inti {

    Integer l1;
    Integer o2;

    public Inti(Integer liste1, Integer liste2) {
        this.l1 = liste1;
        this.o2 = liste2;
    }


    public String toString() {
        return "(" + l1 + " + x = " + o2 + ")\n x = "+ (o2 - l1) + "\n";
    }
}

